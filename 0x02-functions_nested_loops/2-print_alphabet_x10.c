#include "main.h"

/**
 * print_alphabet_x10 - function to print lowercase alphabet ten times
 *
 * Return: 0 always on success
 */

void print_alphabet_x10(void)
{
	char ch;
	int i;

	i = 0;

	while (i < 10)
	{
		ch = 'a';
		while (ch <= 'z')
		{
			_putchar(ch);
			ch++;
		}
		_putchar('\n');
		i++;
	}
}

